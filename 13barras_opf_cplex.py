def pq_to_pu(x_kw_or_kvar, Sbase_ph_kVA=SBASE_PH_KVA):
    return float(x_kw_or_kvar) / Sbase_ph_kVA

def build_line_pu_dicts(df_branches, is_mt=True, rx_is_per_km=False):
    """
    Converte linhas para pu.
    - rx_is_per_km=False: df_branches['R','X'] já estão em ohm TOTAL do trecho (seu caso)
    - rx_is_per_km=True : df_branches['R','X'] estão em ohm/km e usa Length_m
    """
    Zbase = ZBASE_MT_OHM if is_mt else ZBASE_BT_OHM
    Ibase = IBASE_MT_A if is_mt else IBASE_BT_A

    R_pu, X_pu, Imax_pu = {}, {}, {}

    for _, row in df_branches.iterrows():
        m = int(float(row["m"]))
        Imax_A = float(row["Imax"])
        Imax_pu_m = Imax_A / Ibase

        L_km = float(row["Length_m"])/1000.0 if ("Length_m" in df_branches.columns and not pd.isna(row["Length_m"])) else 1.0

        for ph in row["phase_list"]:
            R_raw = float(row["R"])
            X_raw = float(row["X"])

            if rx_is_per_km:
                R_ohm = R_raw * L_km
                X_ohm = X_raw * L_km
            else:
                R_ohm = R_raw
                X_ohm = X_raw

            R_pu[(m, ph)] = R_ohm / Zbase
            X_pu[(m, ph)] = X_ohm / Zbase
            Imax_pu[(m, ph)] = Imax_pu_m

    return R_pu, X_pu, Imax_pu

