    inflow = sum(m.Q_fluxo_MT[br, ph, t] for (br,p) in m.BR_PH_MT if p==ph and m.to_bus_MT[br]==bus)
    outflow= sum(m.Q_fluxo_MT[br, ph, t] for (br,p) in m.BR_PH_MT if p==ph and m.from_bus_MT[br]==bus)

    slack = m.Q_sub_MT[bus, ph, t] if m.Tb[bus]==1 else 0.0
    load  = m.Qd_MT[bus, ph, t]

    to_trafos = sum(
        m.Q_fluxo_mt_to_trafo[tid, ph, t]
        for tid in m.TR
        if (tid, ph) in m.TR_PH and m.trafo_mt_real_bus[tid] == bus
    )

    return inflow - outflow + slack - load - to_trafos + m.Q_inj_svr_MT[bus, ph, t] == 0.0

